// =============================
// Data Types Examples
// =============================

// String
let city = "Pune";
console.log("String:", city);

// Number
let marks = 95;
let pi = 3.14;
console.log("Number (int):", marks);
console.log("Number (float):", pi);

// Boolean
let isStudent = true;
console.log("Boolean:", isStudent);

// Null
let value = null;
console.log("Null:", value);

// Undefined
let data;
console.log("Undefined:", data);

// Symbol
let id1 = Symbol("id");
let id2 = Symbol("id");
console.log("Symbol (unique?):", id1 === id2); // false

// BigInt
let bigNumber = 123456789012345678901234567890n;
console.log("BigInt:", bigNumber);