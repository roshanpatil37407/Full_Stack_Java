// =============================
// Variables & Data Types in JS
// =============================

// 1. Using var
var name = "Roshan"; // String
console.log("var example:", name);

// 2. Using let
let age = 22; // Number
console.log("let example:", age);

// 3. Using const
const country = "India"; // String (constant)
console.log("const example:", country);